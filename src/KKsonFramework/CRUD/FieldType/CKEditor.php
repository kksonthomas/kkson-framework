            let field = $("#field-$name");
            let editor;
            CKEDITOR.ClassicEditor.create(field[0], {
                // https://ckeditor.com/docs/ckeditor5/latest/features/toolbar/toolbar.html#extended-toolbar-configuration-format
                toolbar: {
                    items: [
                        'sourceEditing', '|',
                        'findAndReplace', 'selectAll', '|',
                        'heading', '|',
